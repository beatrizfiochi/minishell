int	ft_strcmp_insensitive(char *s1, char *s2)
{
	int	i;
	int	c1;
	int	c2;

	i = 0;
	while (1)
	{
		c1 = s1[i];
		if (c1 >= 'A' && c1 <= 'Z')
			c1 += ('a' - 'A');
		c2 = s2[i];
		if (c2 >= 'A' && c2 <= 'Z')
			c2 += ('a' - 'A');
		if ((c1 == '\0') || (c2 == '\0') || (c1 != c2))
			break ;
		i++;
	}
	return (c1 - c2);
}
